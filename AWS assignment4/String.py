# Original string
name = "mohanaprasath"

# Convert to upper case
upper_name = name.upper()
print(upper_name)  # "mohanaprasath"

# Convert to lower case
lower_name = name.lower()
print(lower_name)  # "mohanaprasath"

# Capitalize the first letter
capitalized_name = name.capitalize()
print(capitalized_name)  # "mohanaprasath"

# Replace 'p' with 'P'
replaced_name = name.replace('p', 'P')
print(replaced_name)  # "mohanaprasath"